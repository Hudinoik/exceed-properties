// ============================================================
// DocuSign API smoke test — single run.
//
// Exercises six distinct API method types against the currently
// configured environment (demo or prod, decided by the env vars
// the server already reads). The Go-Live review at DocuSign
// requires ≥20 successful API calls across ≥5 method types in
// demo before they'll approve production. One run of this script
// produces 6 successful calls; run the traffic generator
// (docusign-generate-golive-traffic.js) five times to comfortably
// clear both bars.
//
// Methods exercised, in order:
//   1. createEnvelope          (sendLeaseForSignature)
//   2. getEnvelope             (poll until status returned)
//   3. listRecipients
//   4. listDocuments
//   5. listStatusChanges       (fromDate = today-1h)
//   6. getDocument             (downloads the unsigned original PDF)
//
// Usage:
//   TEST_RECIPIENT_EMAIL=you@example.com node scripts/docusign-smoke-test.js
//
// The recipient WILL get an email each run (status='sent'). Use
// a real address you control. Total wall-clock: <30s.
// ============================================================
import '../server/load-env.js';
import { PDFDocument, StandardFonts, rgb } from 'pdf-lib';
import {
  sendLeaseForSignature,
  getEnvelopeStatus,
  listEnvelopes,
  listRecipients,
  listEnvelopeDocuments,
  downloadSignedDocument,
} from '../server/docusign/envelopes.js';
import { isProduction } from '../server/docusign/auth.js';

const TEST_EMAIL = process.env.TEST_RECIPIENT_EMAIL || 'smoke-test@example.invalid';
const TEST_NAME = process.env.TEST_RECIPIENT_NAME || 'Smoke Test Recipient';
// Subject suffix lets the traffic-generator script disambiguate runs.
const SUBJECT_SUFFIX = process.env.TEST_SUBJECT_SUFFIX || '';

// Banner — make it impossible to miss which environment is being hit.
const banner = () => {
  const env = isProduction() ? 'PRODUCTION (live envelopes, real billing)' : 'DEMO';
  // eslint-disable-next-line no-console
  console.log(`\n========================================`);
  // eslint-disable-next-line no-console
  console.log(`DocuSign API smoke test`);
  // eslint-disable-next-line no-console
  console.log(`  Environment: ${env}`);
  // eslint-disable-next-line no-console
  console.log(`  Recipient:   ${TEST_EMAIL}`);
  // eslint-disable-next-line no-console
  console.log(`========================================\n`);
};

// Generate a small, valid PDF in-memory containing the /sig1/ and
// /date1/ anchor strings used by sendLeaseForSignature. Returns a
// Buffer suitable for DocuSign's documentBase64 field.
const buildTestPdf = async (title) => {
  const pdf = await PDFDocument.create();
  const page = pdf.addPage([612, 792]); // US Letter
  const font = await pdf.embedFont(StandardFonts.Helvetica);
  // Visible title + body
  page.drawText('Exceed Props — DocuSign Smoke Test', {
    x: 50, y: 720, size: 16, font, color: rgb(0, 0, 0),
  });
  page.drawText(title, {
    x: 50, y: 695, size: 10, font, color: rgb(0.3, 0.3, 0.3),
  });
  page.drawText('This is an automated test envelope generated by', {
    x: 50, y: 660, size: 11, font, color: rgb(0, 0, 0),
  });
  page.drawText('scripts/docusign-smoke-test.js. You may ignore it.', {
    x: 50, y: 644, size: 11, font, color: rgb(0, 0, 0),
  });
  // Signature anchor box — visible label, then the anchor strings
  // in white-on-white at size 1 so DocuSign locates them but they
  // don't show in the rendered page.
  page.drawText('Signature:', { x: 50, y: 540, size: 11, font });
  page.drawText('/sig1/', {
    x: 50, y: 520, size: 1, font, color: rgb(1, 1, 1),
  });
  page.drawText('Date:', { x: 50, y: 480, size: 11, font });
  page.drawText('/date1/', {
    x: 50, y: 460, size: 1, font, color: rgb(1, 1, 1),
  });
  const bytes = await pdf.save();
  return Buffer.from(bytes);
};

// Format milliseconds as a short string for the results table.
const ms = (start) => `${Date.now() - start}ms`;

const main = async () => {
  banner();

  const results = []; // { method, ok, elapsed, detail }
  let envelopeId = null;

  // ----- 1. createEnvelope --------------------------------------
  {
    const t0 = Date.now();
    try {
      const subject = `Smoke test envelope ${new Date().toISOString().slice(0, 19)}${SUBJECT_SUFFIX ? ' ' + SUBJECT_SUFFIX : ''}`;
      const pdfBuffer = await buildTestPdf(subject);
      const out = await sendLeaseForSignature({
        signerName: TEST_NAME,
        signerEmail: TEST_EMAIL,
        pdfBuffer,
        emailSubject: subject,
        documentName: 'Smoke test',
      });
      envelopeId = out.envelopeId;
      results.push({ method: 'createEnvelope', ok: true, elapsed: ms(t0), detail: `id=${envelopeId} status=${out.status}` });
    } catch (err) {
      results.push({ method: 'createEnvelope', ok: false, elapsed: ms(t0), detail: err.message });
      // Everything else needs an envelope ID; bail to the summary.
      printSummary(results);
      process.exit(1);
    }
  }

  // ----- 2. getEnvelope (poll until status comes back) ----------
  {
    const t0 = Date.now();
    let attempt = 0;
    let lastErr;
    while (attempt < 4) {
      try {
        const env = await getEnvelopeStatus(envelopeId);
        results.push({ method: 'getEnvelope', ok: true, elapsed: ms(t0), detail: `status=${env.status}` });
        break;
      } catch (err) {
        lastErr = err;
        attempt += 1;
        if (attempt < 4) await new Promise(r => setTimeout(r, 5000));
      }
    }
    if (!results.find(r => r.method === 'getEnvelope')) {
      results.push({ method: 'getEnvelope', ok: false, elapsed: ms(t0), detail: lastErr?.message || 'unknown' });
    }
  }

  // ----- 3. listRecipients --------------------------------------
  {
    const t0 = Date.now();
    try {
      const r = await listRecipients(envelopeId);
      const signerCount = Array.isArray(r?.signers) ? r.signers.length : 0;
      results.push({ method: 'listRecipients', ok: true, elapsed: ms(t0), detail: `${signerCount} signer(s)` });
    } catch (err) {
      results.push({ method: 'listRecipients', ok: false, elapsed: ms(t0), detail: err.message });
    }
  }

  // ----- 4. listDocuments ---------------------------------------
  {
    const t0 = Date.now();
    try {
      const d = await listEnvelopeDocuments(envelopeId);
      const docCount = Array.isArray(d?.envelopeDocuments) ? d.envelopeDocuments.length : 0;
      results.push({ method: 'listDocuments', ok: true, elapsed: ms(t0), detail: `${docCount} document(s)` });
    } catch (err) {
      results.push({ method: 'listDocuments', ok: false, elapsed: ms(t0), detail: err.message });
    }
  }

  // ----- 5. listStatusChanges -----------------------------------
  {
    const t0 = Date.now();
    try {
      const fromDate = new Date(Date.now() - 60 * 60 * 1000).toISOString();
      const list = await listEnvelopes({ fromDate });
      const count = Array.isArray(list?.envelopes) ? list.envelopes.length : (list?.resultSetSize ?? 'n/a');
      results.push({ method: 'listStatusChanges', ok: true, elapsed: ms(t0), detail: `${count} envelope(s) since ${fromDate.slice(0, 16)}` });
    } catch (err) {
      results.push({ method: 'listStatusChanges', ok: false, elapsed: ms(t0), detail: err.message });
    }
  }

  // ----- 6. getDocument (returns unsigned original) -------------
  {
    const t0 = Date.now();
    try {
      const buf = await downloadSignedDocument(envelopeId, '1');
      results.push({ method: 'getDocument', ok: true, elapsed: ms(t0), detail: `${buf.length} bytes` });
    } catch (err) {
      results.push({ method: 'getDocument', ok: false, elapsed: ms(t0), detail: err.message });
    }
  }

  printSummary(results);
  // Exit code: 0 if all OK, 1 if any failed.
  process.exit(results.every(r => r.ok) ? 0 : 1);
};

const printSummary = (results) => {
  // eslint-disable-next-line no-console
  console.log(`\n========================================`);
  // eslint-disable-next-line no-console
  console.log(`Results`);
  // eslint-disable-next-line no-console
  console.log(`========================================`);
  const w1 = Math.max(...results.map(r => r.method.length), 10);
  for (const r of results) {
    const status = r.ok ? '✅' : '❌';
    // eslint-disable-next-line no-console
    console.log(`${status}  ${r.method.padEnd(w1)}  ${r.elapsed.padStart(7)}  ${r.detail}`);
  }
  const ok = results.filter(r => r.ok).length;
  // eslint-disable-next-line no-console
  console.log(`\n${ok}/${results.length} methods succeeded.\n`);
};

main().catch((err) => {
  // eslint-disable-next-line no-console
  console.error('Unhandled error:', err);
  process.exit(1);
});
